import java.util.*;
public class loopbasic {
    public static void main(String[] args) {
    Scanner sc =new Scanner(System.in);
    System.out.println("enter the value of n");
    int num =sc.nextInt();
    int counter=1;
    while(counter<=num){
        System.out.println("Hello");
        counter++;
    }
    sc.close();
} 
    
}
