import java.util.Scanner;

public class typecasting {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        char a ='a';
        char b ='b';
        char m ='i';
        System.out.println((int) a);
        System.out.println((int) m);
        System.out.println(a+b);
        sc.close();
    }
}
